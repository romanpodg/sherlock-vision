class ScenarioGenerator:
    async def generate_case(self, setting: str = "Современный детектив в стиле Агаты Кристи", difficulty: str = "Средне", callback=None) -> CaseData | None:
        try:
            logger.info(f"Starting scenario generation for setting: {setting}, difficulty: {difficulty}")
            
            if callback: await callback("🕵️‍♂️ Шаг 1/5: Формируем фабулу...")
            core_story = await self._generate_core(setting, difficulty)
            if not core_story: return None

            if callback: await callback("👥 Шаг 2/5: Создаем подозреваемых...")
            suspects = await self._generate_suspects(core_story, difficulty)
            if not suspects: return None

            if callback: await callback("🔍 Шаг 3/5: Раскладываем улики...")
            evidence = await self._generate_evidence(core_story, suspects, difficulty)
            if not evidence: return None

            if callback: await callback("🗣 Шаг 4/5: Прописываем свидетелей...")
            witnesses = await self._generate_witnesses(core_story, suspects, evidence, difficulty)
            if not witnesses: return None

            if callback: await callback("⚙️ Шаг 5/5: Сборка и проверка логики...")
            case_data_dict = {
                "case_id": f"case_gen_{uuid.uuid4().hex[:8]}",
                "title": core_story.get("title", "Неизвестное дело"),
                "intro": core_story.get("intro", ""),
                "location": core_story.get("location", {}),
                "suspects": suspects,
                "witnesses": witnesses,
                "evidence": evidence,
                "solution": core_story.get("solution", {})
            }

            case = await self._validate_and_repair_json(json.dumps(case_data_dict, ensure_ascii=False))
            
            if case:
                await self._save_to_db(case)
                if callback: await callback("✅ Дело успешно сформировано! Начинаем расследование...")
            else:
                if callback: await callback("❌ Произошла ошибка при сборке дела.")
            return case
        except Exception as e:
            logger.error(f"Error during scenario generation: {e}")
            if callback: await callback(f"❌ Системная ошибка: {e}")
            return None

