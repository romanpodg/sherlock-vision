    def _extract_json(self, text: str) -> str:
        text = text.strip()

        # Убираем markdown-блоки, если они есть
        text = re.sub(r"^```(?:json|JSON)?\s*", "", text)
        text = re.sub(r"\s*```$", "", text)
        text = text.strip()

        # Находим начало JSON (объект или массив)
        start_obj = text.find("{")
        start_arr = text.find("[")

        if start_obj == -1 and start_arr == -1:
            return text

        if start_obj != -1 and (start_arr == -1 or start_obj < start_arr):
            # Ожидаем объект
            end = text.rfind("}")
            if end != -1 and end > start_obj:
                return text[start_obj:end + 1].strip()
        else:
            # Ожидаем массив
            end = text.rfind("]")
            if end != -1 and end > start_arr:
                return text[start_arr:end + 1].strip()

        return text

    def _parse_json(self, text: str) -> Any:
        if not text:
            return None
        try:
            cleaned_text = self._extract_json(text)
            return json.loads(cleaned_text)
        except Exception as e:
            logger.error(f"Failed to parse JSON: {e}\n{text}")
            return None

    async def _validate_and_repair_json(self, raw_json_str: str) -> CaseData | None:
        for attempt in range(3):
            try:
                raw_json_str = self._extract_json(raw_json_str)
                data = json.loads(raw_json_str)
                case = CaseData(**data)
                return case

            except (json.JSONDecodeError, ValidationError) as e:
                logger.warning(f"Validation failed attempt {attempt + 1}: {e}")
                if attempt == 2:
                    logger.error("Failed to repair JSON after 3 attempts.")
                    return None
                # Для Pydantic v2
                try:
                    schema = json.dumps(CaseData.model_json_schema(), ensure_ascii=False, indent=2)
                except AttributeError:
                    # Для Pydantic v1
                    schema = CaseData.schema_json(ensure_ascii=False, indent=2)
                repair_system_prompt = (
                    "Ты — строгий JSON-валидатор и корректор структуры данных для детективной игры Sherlock Vision.\n"
                    "\n"
                    "Твоя задача — исправить только синтаксис JSON и ошибки соответствия схеме Pydantic.\n"
                    "Нельзя переписывать сюжет, менять смысл дела, менять виновного, добавлять новые сюжетные факты или удалять важные данные.\n"
                    "\n"
                    "Отвечай только валидным JSON-объектом.\n"
                    "Не используй markdown-разметку, блоки ```json, комментарии, пояснения, текст до JSON или текст после JSON.\n"
                    "\n"
                    "Правила исправления:\n"
                    "- Сохрани исходную структуру и смысл данных настолько, насколько возможно.\n"
                    "- Исправляй только ошибки JSON, типы данных, отсутствующие обязательные поля и неверные названия полей.\n"
                    "- Не добавляй лишние поля, которых нет в схеме.\n"
                    "- Не меняй id подозреваемых, свидетелей и улик без необходимости.\n"
                    "- Если поле должно быть массивом, сделай его массивом.\n"
                    "- Если поле должно быть boolean, используй true или false без кавычек.\n"
                    "- Если поле должно быть string, используй строку.\n"
                    "- Если значение невозможно восстановить из исходного JSON, заполни его коротким нейтральным значением, не противоречащим делу.\n"
                    "- Итоговый JSON должен проходить json.loads() и валидацию CaseData."
                )
                repair_user_message = (
                    "JSON не прошел валидацию.\n\n"
                    "Ошибка валидации:\n"
                    f"{str(e)}\n\n"
                    "Схема CaseData:\n"
                    f"{schema}\n\n"
                    "Исходный JSON для исправления:\n"
                    f"{raw_json_str}\n\n"
                    "Верни исправленный JSON целиком. Только JSON, без пояснений."
                )
                repaired = await yandex_gpt_client.generate_response(
                    system_prompt=repair_system_prompt,
                    user_message=repair_user_message
                )
                if not repaired:
                    continue
                raw_json_str = repaired.strip()
        return None

